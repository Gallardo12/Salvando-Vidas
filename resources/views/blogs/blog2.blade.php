                    <div class="blog_details">
                        <h2>Médicos en Escocia desarrollaron una nueva técnica quirúrgica que podría significar el fin de las férulas de yeso para ciertas lesiones en las extremidades.
                        </h2>
                        <p class="excert">
                            El procedimiento consiste en un soporte interno que se introduce en el cuerpo con una laparoscopia, una cirugía mínimamente invasiva. Actualmente, cuando ocurre una fractura u otra lesión en alguna extremidad se utiliza una preparación de yeso para inmovilizarla. Pero el profesor Gordon Mackay, cirujano ortopédico del Hospital Ross Hall, en Glasgow, deseaba encontrar una forma de evitar el desgaste muscular que provoca el enyesado y la inconveniencia de tener que usar cabestrillos y botas especiales. 
                        </p>
                        <div class="quote-wrapper">
                            <div class="quotes">
                                "Creo que todos los que han pasado por la experiencia de tener que utilizar una aguja de tejer e introducirla dentro del enyesado para aliviar la picazón, saben que las férulas son extremadamente desagradables" dice el especialista. "También, cuando llega el momento de retirarla, la extremidad tiende a enconarse y los músculos sufren un desgaste enorme", agrega.
                            </div>
                        </div>
                        <h2>Ligamentos dañados</h2>
                        <p>
                            Para evitar todos estos problemas el profesor Mackay utiliza una cirugía de laparsocopia -que requiere una pequeña incisión- para insertar en la una pieza de cinta que actúa como una abrazadera sobre el tejido lesionado. La técnica, por ahora, está siendo probada con ligamentos que sufrieron esguinces y que actualmente requieren inmovilizarse con yeso.
                        </p>
                        <p>
                            La abrazadera, como explica el profesor Mackay, permite el movimiento del tejido pero al mismo tiempo ofrece un apoyo en el ligamente mientras está sanando. Esto significa que el paciente no necesita estar inmovilizado y puede recuperarse mucho más rápido. La técnica es de particular interés para deportistas y atletas que muchas veces deben pasar semanas inmovilizados recuperándose de ligamentos dañados. 
                        </p>
                        <p>Una de las pacientes del profesor Mackay es la patinadora olímpica Sinead Kerr, quien se lesionó cuando patinaba y su pareja artística cayó sobre ella. "No he tenido dolor desde que fui sometida a la cirugía y estoy tomándolo paso por paso. Pero gradualmente estoy volviendo a la normalidad", dice Kerr. "Creo que me ha ayudado mucho el hecho de que no tenido que cargar algo excesivamente rígido en mi brazo". "Y eso me ha permitido llevar a cabo movimientos". </p>
                        <p>El profesor Mackay ya ha llevado a cabo el procedimiento en unos 20 pacientes en Reino Unido y ahora está colaborando con la Clínica Steadman, en Estados Unidos, que se especializa en el tratamiento de heridas de estrellas deportivas. "Si el paciente pude volver a moverse mucho más rápido no desarrolla los problemas secundarios y estará mucho más cómodo". "Además, puede comenzar su rehabilitación de forma inmediata", señala el experto.</p>
